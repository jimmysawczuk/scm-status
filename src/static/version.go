package static

import (
	"bytes"
	"compress/gzip"
	"io"
)

// getVersion returns raw, uncompressed file data.
func getVersion() []byte {
	gz, err := gzip.NewReader(bytes.NewBuffer([]byte{
0x1f,0x8b,0x08,0x00,0x00,0x09,0x6e,0x88,0x00,0xff,0x2a,0x33,
0xd4,0x33,0xd0,0x33,0xe4,0x02,0x04,0x00,0x00,0xff,0xff,0xe5,
0x02,0x73,0x47,0x07,0x00,0x00,0x00,
	}))

	if err != nil {
		panic("Decompression failed: " + err.Error())
	}

	var b bytes.Buffer
	io.Copy(&b, gz)
	gz.Close()

	return b.Bytes()
}