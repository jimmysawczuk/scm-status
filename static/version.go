package static

import (
	"bytes"
	"compress/gzip"
	"io"
)

func getVersion() ([]byte, error) {

	in := bytes.NewBuffer([]byte{
		0x1f, 0x8b, 0x08, 0x00, 0x00, 0x09, 0x6e, 0x88, 0x02, 0xff, 0x2a, 0x33,
		0xd4, 0x33, 0xd0, 0x33, 0xe6, 0x02, 0x04, 0x00, 0x00, 0xff, 0xff, 0x67,
		0x60, 0x45, 0x75, 0x07, 0x00, 0x00, 0x00})
	out := bytes.NewBuffer([]byte{})

	gz, err := gzip.NewReader(in)
	if err != nil {
		return []byte{}, err
	}
	io.Copy(out, gz)

	return out.Bytes(), nil
}
